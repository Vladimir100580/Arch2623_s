# объект, его атрибуты(поля) и методы

print("Hello World".__doc__)    # обращение к объекту doc ("дандер атрибут(или метод)" с двойным подчеркиванием)
print(str.__doc__)

print("Hello world!".upper())
print("Hello world!".count('l'))

# справочные данные dir, help
print(dir("Hello world!"))  # выводит все методы (сначала дандер методы (с __***__))
print(help("Hello world!")) # эта строка не имеет своей документации
